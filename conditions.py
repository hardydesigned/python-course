#Conditions

#1. If

int_number = 1
float_number = 2.5
if int_number > float_number:
    print("Int number is greater than float number")

#2. If-else

if int_number > float_number:
    print("Int number is greater than float number")
else:
    print("Int number is less than float number")

#3. If-elif-else

if int_number > float_number:
    print("Int number is greater than float number")
elif int_number == float_number:
    print("Int number is equal to float number")
else:
    print("Int number is less than float number")

    